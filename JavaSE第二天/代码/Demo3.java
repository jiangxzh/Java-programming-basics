/**
* java中常用的常量
*/
public class Demo3{
	public static void main(String [] args){
		//1.数值常量
		System.out.println(123); //整数
		System.out.println(123.12); //浮点数
		//2.字符常量 （符号： ''）
		System.out.println('a'); // 英文字符
		System.out.println('中'); //中文字符
		//3. 字符串常量 （符号： "",文本）
		System.out.println("中国");
		//4. 布尔常量 true false
		System.out.println(true);
		System.out.println(false);
	}
}